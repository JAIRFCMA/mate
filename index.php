<h2>¿Qué son las integrales dobles y triples?</h2>
<p>Las integrales dobles permiten calcular áreas, volúmenes y masas sobre regiones bidimensionales. Las triples extienden este concepto a regiones tridimensionales.</p>

<h3>Integral doble</h3>
<p>Se representa como: ∬ f(x, y) dA</p>

<h3>Integral triple</h3>
<p>Se representa como: ∭ f(x, y, z) dV</p>

